#!/usr/bin/env node
// aahp-dashboard.mjs - Generate (and gate) the drift-prone handoff state docs
// DASHBOARD.md and TRUST.md from committed repo data.
//
// Two modes:
//   node scripts/aahp-dashboard.mjs          -> WRITE the docs (npm run handoff:refresh)
//   node scripts/aahp-dashboard.mjs --check  -> FAIL if the committed docs are stale
//                                               (wired into `prebuild`, like
//                                               check:changelog / check:version-sync)
//
// The output is a PURE FUNCTION of committed files (package.json + tsconfig +
// the src/ file list) - no timestamps, no HEAD sha, no live `npm audit` - so:
//   - regenerating an unchanged repo is a no-op (no churn), and
//   - the --check gate is deterministic and offline.
// That is what makes these docs impossible to SILENTLY drift: any real change
// to version / modules / deps makes the build red until `handoff:refresh` runs.
// Build/test PASS status and vulnerability counts are time-varying, so they are
// NOT snapshotted here - they live in CI (the authoritative, always-current
// source) and dependabot.

import { readFileSync, readdirSync, writeFileSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const handoff = join(repoRoot, ".ai", "handoff");
const rd = (p) => readFileSync(join(repoRoot, p), "utf8");

const pkg = JSON.parse(rd("package.json"));

// --- derive facts (deterministic, offline) ----------------------------------

const modules = readdirSync(join(repoRoot, "src"))
  .filter((f) => f.endsWith(".ts"))
  .sort();
const testFiles = readdirSync(join(repoRoot, "src", "__tests__"))
  .filter((f) => f.endsWith(".test.ts"))
  .sort();

const tsconfig = existsSync(join(repoRoot, "tsconfig.json")) ? rd("tsconfig.json") : "";
const typesNode = /"types"\s*:\s*\[\s*"node"\s*\]/.test(tsconfig) ? "yes" : "NO (TS6 build will break)";

const deps = { ...(pkg.dependencies || {}), ...(pkg.devDependencies || {}) };
const depRows = Object.entries(deps)
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([n, v]) => `| ${n} | ${v} |`)
  .join("\n");

const moduleList = modules.map((m) => `\`${m}\``).join(", ");

const header = (title) =>
  `# supply-chain-guard: ${title}\n\n` +
  `> AUTO-GENERATED by scripts/aahp-dashboard.mjs. Do NOT hand-edit.\n` +
  `> Run \`npm run handoff:refresh\` to update. \`npm run check:handoff\` (in prebuild)\n` +
  `> fails the build if this file is stale, so it cannot silently drift. Content is a\n` +
  `> pure function of package.json + tsconfig + the src/ file list.\n`;

// --- DASHBOARD.md -----------------------------------------------------------

const dashboard = `${header("Build Dashboard")}
---

## Pipeline State

| Field | Value |
|-------|-------|
| Version | ${pkg.version} |
| Node engines | ${pkg.engines?.node ?? "unspecified"} |
| Source modules | ${modules.length} under \`src/\` |
| Test files | ${testFiles.length} under \`src/__tests__/\` |
| tsconfig \`types: ["node"]\` | ${typesNode} |
| Build / test / audit | verified continuously in CI - see below |

Time-varying facts (build/test pass-fail, \`npm audit\`) are intentionally not
snapshotted here: they are verified on every push in CI (the authoritative source)
and by dependabot. Locally, \`npm test\` shows 13 vscode-scanner failures that only
reflect a missing \`zip\` binary (green on Ubuntu CI); not regressions.

---

## Toolchain

| Package | Range |
|---------|-------|
${depRows}

Overrides: ${pkg.overrides ? "`" + JSON.stringify(pkg.overrides) + "`" : "none"}.

---

## Source Modules (${modules.length})

${moduleList}

---

## Infrastructure

| Component | Notes |
|-----------|-------|
| GitHub repo | homeofe/supply-chain-guard (Apache-2.0) |
| CI (\`ci.yml\`) | build+test on push/PR; on semver tags: OIDC npm publish, GitHub Release, \`v5\` branch fast-forward |
| AAHP Verify (\`aahp-verify.yml\`) | handoff gate; dependabot exempt |
| Prebuild gates | \`check:changelog\` + \`check:version-sync\` + \`check:handoff\` |
| npm publish | OIDC trusted publishing (no NPM_TOKEN); needs npm >=11.5.1 in CI |
| GitHub Action | composite; \`uses: homeofe/supply-chain-guard@v5\` (floating branch) |
| Dependabot | weekly npm + github-actions; commander >=15 ignored |

---

## Open Tasks

See NEXT_ACTIONS.md (the one hand-curated backlog doc). Task status lives in
MANIFEST.json \`tasks\`.
`;

// --- TRUST.md ---------------------------------------------------------------

const trust = `${header("Trust Register")}
> Build/test/audit verification is CONTINUOUS in CI (the Actions tab: "Build and
> Test" + "AAHP Verify") and dependabot - that is the live source of truth, always
> current, and runs on Linux without the local \`zip\` gap. This file records only
> the deterministic structural facts, kept fresh by \`check:handoff\`.

---

## Structural facts (derived from committed files, gate-enforced fresh)

| Property | Value | Source |
|----------|-------|--------|
| package.json version | ${pkg.version} | package.json |
| Source modules present | ${modules.length} | src/ file list |
| Test files present | ${testFiles.length} | src/__tests__/ file list |
| tsconfig \`types: ["node"]\` | ${typesNode} | tsconfig.json (load-bearing under TS6) |
| Runtime dependency | commander ${deps.commander ?? "?"} | package.json (CommonJS line; 15+ is ESM-only) |

---

## Continuously verified in CI (per push, authoritative)

| Property | Where |
|----------|-------|
| \`npm run build\` (tsc) passes | CI "Build and Test" job |
| \`npm test\` (vitest) passes | CI "Build and Test" job (Linux) |
| \`npm audit\` clean | CI + dependabot |
| AAHP handoff gate passes | "AAHP Verify" workflow |
| OIDC npm publish + GitHub Release + \`v5\` fast-forward | CI, on semver tags only |
`;

// --- write or check ---------------------------------------------------------

const targets = [
  ["DASHBOARD.md", dashboard],
  ["TRUST.md", trust],
];
const norm = (s) => s.replace(/\r/g, ""); // CRLF-agnostic (Windows working tree)

if (process.argv.includes("--check")) {
  const stale = targets.filter(([name, content]) => {
    const p = join(handoff, name);
    const current = existsSync(p) ? readFileSync(p, "utf8") : "";
    return norm(current) !== norm(content);
  });
  if (stale.length > 0) {
    console.error(
      `\n  Handoff docs are stale: ${stale.map(([n]) => n).join(", ")}.\n` +
        `  These are generated from package.json + tsconfig + the src/ file list.\n` +
        `  Fix: run \`npm run handoff:refresh\`, then re-commit.\n`,
    );
    process.exit(1);
  }
  console.log("Handoff docs up to date: DASHBOARD.md, TRUST.md.");
} else {
  for (const [name, content] of targets) writeFileSync(join(handoff, name), content);
  console.log(
    `handoff:refresh OK - DASHBOARD.md + TRUST.md regenerated ` +
      `(modules=${modules.length}, testFiles=${testFiles.length}, types:[node]=${typesNode}).`,
  );
}
