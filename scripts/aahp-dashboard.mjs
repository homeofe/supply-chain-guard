#!/usr/bin/env node
// aahp-dashboard.mjs - Regenerate the drift-prone handoff state docs
// (DASHBOARD.md and TRUST.md) from LIVE repo data instead of by hand.
//
// Why: the AAHP content-drift gate only forces STATUS.md + MANIFEST.json to
// change per commit, so hand-maintained snapshot docs (DASHBOARD, TRUST) rot.
// They sat at v3.1.0 for ~40 releases. These two are now derived, not curated:
//   - version / deps / engines  <- package.json
//   - module + test-file counts <- src/ and src/__tests__/
//   - vulnerabilities           <- npm audit --json
//   - HEAD commit               <- git
// Build/test PASS status is intentionally NOT snapshotted here: it is verified
// continuously in CI on every push (the real source of truth), and running the
// suite locally on Windows shows false reds (the vscode tests need a `zip`
// binary). This file reports structure + points to CI for pass/fail.
//
// Usage: node scripts/aahp-dashboard.mjs   (or: npm run handoff:refresh)
// After running, regenerate the manifest so checksums match:
//   bash scripts/aahp-manifest.sh . --agent <id> --phase idle

import { readFileSync, readdirSync, writeFileSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";
import { execSync } from "node:child_process";

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const handoff = join(repoRoot, ".ai", "handoff");
const rd = (p) => readFileSync(join(repoRoot, p), "utf8");

const pkg = JSON.parse(rd("package.json"));
const today = new Date().toISOString().slice(0, 10);

// --- derive facts -----------------------------------------------------------

const modules = readdirSync(join(repoRoot, "src"))
  .filter((f) => f.endsWith(".ts"))
  .sort();
const testFiles = readdirSync(join(repoRoot, "src", "__tests__"))
  .filter((f) => f.endsWith(".test.ts"))
  .sort();

let head = "unknown";
try {
  head = execSync("git rev-parse --short HEAD", { cwd: repoRoot }).toString().trim();
} catch {
  /* not a git repo */
}

let vulns = "unknown";
try {
  // npm audit exits non-zero when vulns exist, so read stdout either way.
  const out = execSync("npm audit --json", { cwd: repoRoot, stdio: ["ignore", "pipe", "ignore"] }).toString();
  vulns = String(JSON.parse(out).metadata.vulnerabilities.total);
} catch (err) {
  try {
    vulns = String(JSON.parse(err.stdout.toString()).metadata.vulnerabilities.total);
  } catch {
    vulns = "unknown";
  }
}

const tsconfig = existsSync(join(repoRoot, "tsconfig.json")) ? rd("tsconfig.json") : "";
const typesNode = /"types"\s*:\s*\[\s*"node"\s*\]/.test(tsconfig) ? "yes" : "NO (TS6 build will break)";

const deps = { ...(pkg.dependencies || {}), ...(pkg.devDependencies || {}) };
const depRows = Object.entries(deps)
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([n, v]) => `| ${n} | ${v} |`)
  .join("\n");

const moduleList = modules.map((m) => `\`${m}\``).join(", ");
const genHeader = (title) =>
  `# supply-chain-guard: ${title}\n\n` +
  `> AUTO-GENERATED by scripts/aahp-dashboard.mjs (\`npm run handoff:refresh\`).\n` +
  `> Do not hand-edit; changes are overwritten. STATUS.md is the hand-maintained state doc.\n` +
  `> Generated: ${today} from commit ${head}.\n`;

// --- DASHBOARD.md -----------------------------------------------------------

const dashboard = `${genHeader("Build Dashboard")}
---

## Pipeline State

| Field | Value |
|-------|-------|
| Version | ${pkg.version} |
| Node engines | ${pkg.engines?.node ?? "unspecified"} |
| Source modules | ${modules.length} under \`src/\` |
| Test files | ${testFiles.length} under \`src/__tests__/\` |
| npm audit | ${vulns === "0" ? "0 vulnerabilities" : vulns + " vulnerabilities"} |
| tsconfig \`types: ["node"]\` | ${typesNode} |
| Build / test pass-fail | see CI (Actions) - authoritative on Linux |

Exact test pass/fail counts are verified continuously in CI on every push. Locally,
\`npm test\` shows 13 vscode-scanner failures that only reflect a missing \`zip\` binary
(green on Ubuntu CI); do not treat those as regressions.

---

## Toolchain

| Package | Range |
|---------|-------|
${depRows}

Overrides: ${pkg.overrides ? "`" + JSON.stringify(pkg.overrides) + "`" : "none"}.

---

## Source Modules (${modules.length})

${moduleList}

---

## Infrastructure

| Component | Status | Notes |
|-----------|--------|-------|
| GitHub repo | active | homeofe/supply-chain-guard (Apache-2.0) |
| CI (\`ci.yml\`) | active | build+test on push/PR; on semver tags: OIDC npm publish, GitHub Release, \`v5\` branch fast-forward |
| AAHP Verify (\`aahp-verify.yml\`) | active | handoff gate; dependabot exempt |
| Prebuild gates | active | \`check:changelog\` + \`check:version-sync\` block release drift |
| npm publish | active | OIDC trusted publishing (no NPM_TOKEN); needs npm >=11.5.1 in CI |
| GitHub Action | active | composite; \`uses: homeofe/supply-chain-guard@v5\` (floating branch) |
| Dependabot | active | weekly npm + github-actions; commander >=15 ignored |

---

## Open Tasks

See NEXT_ACTIONS.md (the one hand-curated backlog doc). Task status lives in
MANIFEST.json \`tasks\`.
`;

// --- TRUST.md ---------------------------------------------------------------

const trust = `${genHeader("Trust Register")}
> Machine-checkable facts as of generation. Build/test verification is CONTINUOUS in
> CI (the Actions tab: "Build and Test" + "AAHP Verify") - that is the live source of
> truth, always current, and runs on Linux without the local \`zip\` gap.

---

## Verified at generation time (${today})

| Property | Result | How |
|----------|--------|-----|
| npm audit | ${vulns === "0" ? "0 vulnerabilities" : vulns + " vulnerabilities"} | \`npm audit --json\` |
| package.json version | ${pkg.version} | package.json |
| Source modules present | ${modules.length} | filesystem |
| Test files present | ${testFiles.length} | filesystem |
| tsconfig \`types: ["node"]\` | ${typesNode} | tsconfig.json (load-bearing under TS6) |
| HEAD commit | ${head} | git |

---

## Continuously verified in CI (per push, authoritative)

| Property | Where |
|----------|-------|
| \`npm run build\` (tsc) passes | CI "Build and Test" job |
| \`npm test\` (vitest) passes | CI "Build and Test" job (Linux) |
| AAHP handoff gate passes | "AAHP Verify" workflow |
| OIDC npm publish + GitHub Release + \`v5\` fast-forward | CI, on semver tags only |

To refresh this file: \`npm run handoff:refresh\`.
`;

writeFileSync(join(handoff, "DASHBOARD.md"), dashboard);
writeFileSync(join(handoff, "TRUST.md"), trust);

console.log(
  `handoff:refresh OK - DASHBOARD.md + TRUST.md regenerated for v${pkg.version} @ ${head}\n` +
    `  modules=${modules.length} testFiles=${testFiles.length} audit=${vulns} types:[node]=${typesNode}\n` +
    `  next: bash scripts/aahp-manifest.sh . --agent <id> --phase idle`,
);
